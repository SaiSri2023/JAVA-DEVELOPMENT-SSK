    class Color {
        public void printColor() {
            System.out.println("Welcome to colorful world");
        }
    }

    class Black extends Color {
        @Override
        public void printColor() {
            System.out.println("Black world");
        }
    }

    class Orange extends Color {
        @Override
        public void printColor() {
            System.out.println("Orange World");
        }
    }
    class Blue extends Color {
        @Override
        public void printColor() {
            System.out.println("Blue World");
        }
    }

